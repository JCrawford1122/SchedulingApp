package Models;

/**
 *
 * @author crawf
 * ConsultantReport is a class used for the purpose of generating a report
 * consisting of data from the customer table joined with data from the appointment
 * table
 */
public class ConsultantReport {
    private String customerName;
    private String type;
    private String time;

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
    
}
